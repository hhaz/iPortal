@interface HomePageTableViewController ()
@property (nonatomic, strong) NSNumber *numberOfTrx;
@property (nonatomic, strong) NSNumber *amountOfTrx;

@end

@implementation HomePageTableViewController



NSArray *menuItemsHome;

- (void)viewDidLoad {
    [super viewDidLoad];
    
    NSNumberFormatter *formatter = [[NSNumberFormatter alloc] init];
    NSString *groupingSeparator = [[NSLocale currentLocale] objectForKey:NSLocaleGroupingSeparator];
    [formatter setGroupingSeparator:groupingSeparator];
    [formatter setGroupingSize:3];
    [formatter setAlwaysShowsDecimalSeparator:NO];
    [formatter setUsesGroupingSeparator:YES];
    
    _appDelegate = (AppDelegate *)[[UIApplication sharedApplication] delegate];
    
    menuItemsHome = @[@"NbTransactions", @"AmountTransactions", @"TNAs", @"CreditTransactions"];
    
    SWRevealViewController *revealViewController = self.revealViewController;
    if ( revealViewController )
    {
        [self.sidebarButton setTarget: self.revealViewController];
        [self.sidebarButton setAction: @selector( revealToggle: )];
        [self.view addGestureRecognizer:self.revealViewController.panGestureRecognizer];
    }
    
    getStats *retrieveStats = [[getStats alloc]init];
    
    [retrieveStats getData:self.tableView nbTrx:_labelNbTrx amountTrx:_labelAmountTrx];
    
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

